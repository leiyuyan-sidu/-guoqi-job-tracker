-- 一次性回溯修正：把已经入库、但其实是蓝领/技能岗位的记录改成不可报名
-- （之后新抓取的岗位会由 scraper 自动排除，这段只用于清理历史数据，只需跑一次）
update jobs
set eligible = false,
    eligible_reason = '学历要求过低或为技能/蓝领岗位，回溯修正'
where eligible = true
  and (
    education in ('大专', '中专', '中专/中技', '中技', '高中', '职高', '技校')
    or title ~ '厨师|厨工|配菜|摆盘|洗碗|技工|维修工|电工|焊工|钳工|机修工|运维技工|驾驶员|司机|保安|保洁|安保|营业员|服务员|收银员|驱鸟员|起重工|行车工'
  );
